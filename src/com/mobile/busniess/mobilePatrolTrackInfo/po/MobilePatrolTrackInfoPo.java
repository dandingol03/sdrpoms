package com.mobile.busniess.mobilePatrolTrackInfo.po;

/**   
 * @Package: com.mobile.busniess.mobilePatrolTrackInfo.po 
 * @author: jwl   
 * @date: 2018年5月19日 下午4:51:29 
 */
public class MobilePatrolTrackInfoPo {
	private String id;					//ID
	private String mid;                 //ID移动端区分
	private String userId;				//巡防人	
	private String startTime;			//任务开始时间
	private String endTime;				//任务完成时间
	private String track;				//巡防轨迹
	private String remark;				//备注
	
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getTrack() {
		return track;
	}
	public void setTrack(String track) {
		this.track = track;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
