package com.mobile.bsp.menu.po;

/**
 * 移动端菜单PO
 * @author D.Steven
 *
 */
public class MobileMenu {
	
	private String mobileMenuId;			//ID
	private String mobileMenuName;			//移动端菜单名称
	private String mobileMenuUrl;			//移动端菜单URL
	private String mobileMenuType;			//移动端是否为父节点 1为父节点 0为子节点
	private String mobileMenuPid;			//移动端父节点ID
	private String mobileMenuDesc;			//移动端排序
	private String mobileOpenType;			//移动端打开类型 a 为ajax i为iframe
	private String mobileMenuIcon;			//移动端菜单图标
	
	public String getMobileMenuId() {
		return mobileMenuId;
	}
	public void setMobileMenuId(String mobileMenuId) {
		this.mobileMenuId = mobileMenuId;
	}
	public String getMobileMenuName() {
		return mobileMenuName;
	}
	public void setMobileMenuName(String mobileMenuName) {
		this.mobileMenuName = mobileMenuName;
	}
	public String getMobileMenuUrl() {
		return mobileMenuUrl;
	}
	public void setMobileMenuUrl(String mobileMenuUrl) {
		this.mobileMenuUrl = mobileMenuUrl;
	}
	public String getMobileMenuType() {
		return mobileMenuType;
	}
	public void setMobileMenuType(String mobileMenuType) {
		this.mobileMenuType = mobileMenuType;
	}
	public String getMobileMenuPid() {
		return mobileMenuPid;
	}
	public void setMobileMenuPid(String mobileMenuPid) {
		this.mobileMenuPid = mobileMenuPid;
	}
	public String getMobileMenuDesc() {
		return mobileMenuDesc;
	}
	public void setMobileMenuDesc(String mobileMenuDesc) {
		this.mobileMenuDesc = mobileMenuDesc;
	}
	public String getMobileOpenType() {
		return mobileOpenType;
	}
	public void setMobileOpenType(String mobileOpenType) {
		this.mobileOpenType = mobileOpenType;
	}
	public String getMobileMenuIcon() {
		return mobileMenuIcon;
	}
	public void setMobileMenuIcon(String mobileMenuIcon) {
		this.mobileMenuIcon = mobileMenuIcon;
	}

	
}
