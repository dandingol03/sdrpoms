public class DangerRangeGPS {
	private double X1;
	private double Y1;
	private double X2;
	private double Y2;
	
	
	public double getX1() {
		return X1;
	}

	public void setX1(double x1) {
		X1 = x1;
	}

	public double getY1() {
		return Y1;
	}

	public void setY1(double y1) {
		Y1 = y1;
	}

	public double getX2() {
		return X2;
	}

	public void setX2(double x2) {
		X2 = x2;
	}

	public double getY2() {
		return Y2;
	}

	public void setY2(double y2) {
		Y2 = y2;
	}

	@Override
	public String toString() {
		return "POLYGON(("+X1+" "+Y1+", "+X2+" "+Y1+", "+X2+" "+Y2+","+X1+" "+Y2+","+X1+" "+Y1+"))";
	}


	
}
